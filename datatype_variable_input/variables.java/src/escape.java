public class escape{
    public static void main(String[] args){
        System.out.println("Hello \"Arpit\""); // For double or single quote we use escape sequence \" or \'
        System.out.println("Hello \tArpit"); // For tab we use escape sequence \t
        System.out.println("Hello \nArpit"); // For new line we use escape sequence \n
        System.out.println("Hello \bArpit"); // For backspace we use escape sequence \b
    }
}